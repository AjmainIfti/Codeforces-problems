#include<bits/stdc++.h>
using namespace std;
 
int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
 
    long long n;
    cin>>n;
    int lucky=0;
    while(n>0){
    int a = n%10;
    if(a==4 || a==7)
        lucky++;
    n/=10;
    }
 
    if(lucky==4 || lucky==7)
        cout<<"YES";
    else
        cout<<"NO";
}
