#include<bits/stdc++.h>
using namespace std;
 
int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
 
    int k, n, w, price=0;
    cin>>k>>n>>w;
    int i;
    for(i=1; i<=w; i++){
        price+=(i*k);
    }
    int borrow = price - n;
    if(borrow<0)
        borrow=0;
    cout<<borrow;
 
}
